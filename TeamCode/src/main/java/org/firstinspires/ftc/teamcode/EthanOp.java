package org.firstinspires.ftc.teamcode;

import com.qualcomm.robotcore.eventloop.opmode.OpMode;
import com.qualcomm.robotcore.eventloop.opmode.TeleOp;

import static org.firstinspires.ftc.teamcode.BreakoutMotor.Direction.MOTOR_F;
import static org.firstinspires.ftc.teamcode.BreakoutMotor.Direction.MOTOR_R;

@TeleOp (name = "EthanOp", group = "Pushbot")
public class EthanOp extends OpMode {

    private BreakoutMotor motorLeftFront = new BreakoutMotor();
    private BreakoutMotor motorRightFront = new BreakoutMotor();
    private BreakoutMotor motorLeftBack = new BreakoutMotor();
    private BreakoutMotor motorRightBack = new BreakoutMotor();


    @Override
    public void init() {
        motorLeftFront.set(hardwareMap.dcMotor.get("motorLeftFront"));
        motorRightFront.set(hardwareMap.dcMotor.get("motorRightFront"));
        motorLeftBack.set(hardwareMap.dcMotor.get("motorLeftBack"));
        motorRightBack.set(hardwareMap.dcMotor.get("motorRightBack"));
        motorLeftFront.setDirection(MOTOR_R);
        motorRightFront.setDirection(MOTOR_F);
        motorLeftBack.setDirection(MOTOR_R);
        motorRightBack.setDirection(MOTOR_F);
        motorLeftFront.setPower(0);
        motorRightFront.setPower(0);
        motorLeftBack.setPower(0);
        motorRightBack.setPower(0);
    }


    @Override
    public void start() {
        motorLeftFront.setPower(0);
        motorRightFront.setPower(0);
        motorLeftBack.setPower(0);
        motorRightBack.setPower(0);
    }


    @Override
    public void loop() {
        //Gamepad 1
        float OneStickRightX = gamepad1.right_stick_x;
        float OneStickRightY = gamepad1.right_stick_y;



    }

    @Override
    public void stop() {

    }


}


















































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































